package edu.illinois.finalproject;

public class Room {
    private String Access;
    private String DJ;
    private String Name;
    private Double Latitude;
    private Double Longitude;

    public String getAccess() {
        return Access;
    }

    public String getDJ() {
        return DJ;
    }

    public String getName() {
        return Name;
    }

    public Double getLatitude() {
        return Latitude;
    }

    public Double getLongitude() {
        return Longitude;
    }
}
